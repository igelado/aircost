-- Clean aircraft identity and reference-configuration catalog.
--
-- This additive migration creates the shadow/canonical target model without
-- copying or approving any legacy aircraft/configuration data. Apply to a
-- backup first.

BEGIN;

CREATE TABLE curation_evidence_sources (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  source_url TEXT NOT NULL,
  resolved_url TEXT,
  source_title TEXT NOT NULL,
  publisher TEXT,
  source_domain TEXT NOT NULL,
  source_tier TEXT NOT NULL CHECK (source_tier IN (
    'manufacturer_primary', 'regulator_primary', 'recognized_secondary',
    'marketplace_observation'
  )),
  content_sha256 TEXT,
  retrieved_at TEXT NOT NULL,
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (source_url, content_sha256)
);

CREATE INDEX idx_curation_evidence_sources_domain
  ON curation_evidence_sources (source_domain, source_tier);

CREATE TABLE curation_evidence_claims (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  evidence_source_id BIGINT NOT NULL
    REFERENCES curation_evidence_sources(id) ON DELETE RESTRICT,
  claim_kind TEXT NOT NULL CHECK (claim_kind IN (
    'identity', 'alias', 'applicability', 'standard_equipment', 'price',
    'specification', 'package_composition', 'other'
  )),
  subject_text TEXT NOT NULL,
  predicate_text TEXT NOT NULL,
  object_text TEXT NOT NULL,
  quoted_evidence TEXT NOT NULL,
  citation_start BIGINT,
  citation_end BIGINT,
  validation_status TEXT NOT NULL DEFAULT 'captured'
    CHECK (validation_status IN ('captured', 'validated', 'rejected')),
  validated_at TEXT,
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CHECK (length(trim(quoted_evidence)) > 0),
  CHECK (
    (citation_start IS NULL AND citation_end IS NULL)
    OR (
      citation_start IS NOT NULL AND citation_start >= 0
      AND citation_end IS NOT NULL AND citation_end > citation_start
    )
  ),
  CHECK (
    validation_status <> 'validated'
    OR validated_at IS NOT NULL
  )
);

CREATE INDEX idx_curation_evidence_claims_source
  ON curation_evidence_claims (evidence_source_id, claim_kind);

CREATE OR REPLACE FUNCTION preserve_curation_evidence_source()
RETURNS TRIGGER AS $$
BEGIN
  RAISE EXCEPTION 'curation evidence sources are immutable';
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER curation_evidence_sources_immutable
BEFORE UPDATE OR DELETE ON curation_evidence_sources
FOR EACH ROW EXECUTE FUNCTION preserve_curation_evidence_source();

CREATE OR REPLACE FUNCTION validate_curation_evidence_claim_once()
RETURNS TRIGGER AS $$
BEGIN
  IF TG_OP = 'DELETE' THEN
    RAISE EXCEPTION 'curation evidence claims are immutable';
  END IF;
  IF OLD.validation_status <> 'captured'
     OR NEW.validation_status NOT IN ('validated', 'rejected')
     OR NEW.evidence_source_id IS DISTINCT FROM OLD.evidence_source_id
     OR NEW.claim_kind IS DISTINCT FROM OLD.claim_kind
     OR NEW.subject_text IS DISTINCT FROM OLD.subject_text
     OR NEW.predicate_text IS DISTINCT FROM OLD.predicate_text
     OR NEW.object_text IS DISTINCT FROM OLD.object_text
     OR NEW.quoted_evidence IS DISTINCT FROM OLD.quoted_evidence
     OR NEW.citation_start IS DISTINCT FROM OLD.citation_start
     OR NEW.citation_end IS DISTINCT FROM OLD.citation_end THEN
    RAISE EXCEPTION 'curation evidence claims are append-only and validate once';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER curation_evidence_claims_immutable
BEFORE UPDATE OR DELETE ON curation_evidence_claims
FOR EACH ROW EXECUTE FUNCTION validate_curation_evidence_claim_once();

