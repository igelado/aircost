CREATE OR REPLACE FUNCTION validate_aircraft_catalog_relationship()
RETURNS TRIGGER AS $$
DECLARE
  designation_family_id BIGINT;
  generation_family_id BIGINT;
  package_family_id BIGINT;
BEGIN
  IF TG_TABLE_NAME = 'aircraft_generation_designations' THEN
    SELECT aircraft_model_family_id INTO generation_family_id
    FROM aircraft_generations WHERE id = NEW.aircraft_generation_id;
    SELECT aircraft_model_family_id INTO designation_family_id
    FROM aircraft_designations WHERE id = NEW.aircraft_designation_id;
    IF generation_family_id IS DISTINCT FROM designation_family_id THEN
      RAISE EXCEPTION 'generation and designation must belong to one family';
    END IF;
  ELSIF TG_TABLE_NAME = 'aircraft_package_applicability' THEN
    SELECT aircraft_model_family_id INTO package_family_id
    FROM aircraft_factory_packages WHERE id = NEW.aircraft_factory_package_id;
    SELECT aircraft_model_family_id INTO designation_family_id
    FROM aircraft_designations WHERE id = NEW.aircraft_designation_id;
    IF NEW.aircraft_generation_id IS NOT NULL THEN
      SELECT aircraft_model_family_id INTO generation_family_id
      FROM aircraft_generations WHERE id = NEW.aircraft_generation_id;
    END IF;
    IF package_family_id IS DISTINCT FROM designation_family_id
       OR (NEW.aircraft_generation_id IS NOT NULL
           AND generation_family_id IS DISTINCT FROM designation_family_id) THEN
      RAISE EXCEPTION 'package applicability dimensions must belong to one family';
    END IF;
  ELSIF TG_TABLE_NAME = 'aircraft_reference_configurations' THEN
    IF NEW.aircraft_generation_id IS NOT NULL AND NOT EXISTS (
      SELECT 1 FROM aircraft_generation_designations link
      WHERE link.aircraft_generation_id = NEW.aircraft_generation_id
        AND link.aircraft_designation_id = NEW.aircraft_designation_id
    ) THEN
      RAISE EXCEPTION 'reference configuration generation is not applicable to designation';
    END IF;
    IF NEW.tier_package_id IS NOT NULL AND NOT EXISTS (
      SELECT 1
      FROM aircraft_factory_packages package
      JOIN aircraft_package_applicability applicability
        ON applicability.aircraft_factory_package_id = package.id
      WHERE package.id = NEW.tier_package_id
        AND package.package_kind = 'trim_tier'
        AND applicability.aircraft_designation_id = NEW.aircraft_designation_id
        AND (
          applicability.aircraft_generation_id IS NULL
          OR applicability.aircraft_generation_id = NEW.aircraft_generation_id
        )
    ) THEN
      RAISE EXCEPTION 'reference configuration tier is not applicable to designation/generation';
    END IF;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

