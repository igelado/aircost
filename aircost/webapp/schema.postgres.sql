CREATE TABLE IF NOT EXISTS aircraft_model_spec_versions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  aircraft_model_id BIGINT NOT NULL REFERENCES aircraft_models(id),
  aircraft_model_variant_id BIGINT NOT NULL REFERENCES aircraft_model_variants(id),
  effective_from TEXT NOT NULL,
  effective_to TEXT,
  depreciation_profile_id BIGINT REFERENCES depreciation_profiles(id),
  average_inflation_rate DOUBLE PRECISION NOT NULL DEFAULT 0.025,
  fuel_burn_gph DOUBLE PRECISION,
  oil_quarts_per_hour DOUBLE PRECISION,
  oil_price_per_quart_usd DOUBLE PRECISION,
  engine_model_id BIGINT REFERENCES engine_models(id),
  engine_count BIGINT NOT NULL DEFAULT 1,
  engine_tbo_hours DOUBLE PRECISION,
  engine_overhaul_cost_usd DOUBLE PRECISION,
  engine_value_baseline_life_fraction DOUBLE PRECISION NOT NULL DEFAULT 0.5,
  propeller_model_id BIGINT REFERENCES propeller_models(id),
  propeller_count BIGINT NOT NULL DEFAULT 1,
  propeller_tbo_hours DOUBLE PRECISION,
  propeller_overhaul_cost_usd DOUBLE PRECISION,
  propeller_value_baseline_life_fraction DOUBLE PRECISION NOT NULL DEFAULT 0.5,
  annual_inspection_usd DOUBLE PRECISION,
  other_maintenance_per_hour DOUBLE PRECISION,
  source_url TEXT,
  created_by_user_id BIGINT REFERENCES users(id),
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CHECK (effective_to IS NULL OR effective_to > effective_from)
