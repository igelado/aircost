CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email TEXT NOT NULL UNIQUE,
  display_name TEXT NOT NULL,
  auth_provider TEXT NOT NULL DEFAULT 'local',
  auth_subject TEXT NOT NULL UNIQUE,
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS depreciation_profiles (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  age_decay_rate DOUBLE PRECISION NOT NULL,
  long_run_residual_fraction DOUBLE PRECISION NOT NULL,
  new_to_used_discount_fraction DOUBLE PRECISION NOT NULL,
  new_to_used_discount_years DOUBLE PRECISION NOT NULL,
  airframe_doubling_discount DOUBLE PRECISION NOT NULL,
  max_airframe_premium DOUBLE PRECISION NOT NULL,
  max_airframe_discount DOUBLE PRECISION NOT NULL,
  replacement_floor_fraction DOUBLE PRECISION NOT NULL DEFAULT 0,
  minimum_value_fraction DOUBLE PRECISION NOT NULL,
  high_time_threshold_hours DOUBLE PRECISION,
  high_time_discount_at_double_threshold DOUBLE PRECISION NOT NULL,
  is_system_profile BOOLEAN NOT NULL DEFAULT FALSE,
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS depreciation_profile_fit_metadata (
  depreciation_profile_id BIGINT PRIMARY KEY
    REFERENCES depreciation_profiles(id) ON DELETE CASCADE,
  fit_scope TEXT NOT NULL CHECK (fit_scope IN ('global', 'category', 'model')),
  fit_scope_key TEXT NOT NULL,
  fit_category TEXT NOT NULL,
  sample_count BIGINT NOT NULL,
  rmse_usd DOUBLE PRECISION NOT NULL,
  mae_fraction DOUBLE PRECISION NOT NULL,
  created_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (fit_scope, fit_scope_key)
);

CREATE INDEX IF NOT EXISTS idx_depreciation_profile_fit_metadata_category
  ON depreciation_profile_fit_metadata (fit_category);

